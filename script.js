function assign_rep_name(){
    for(let i=0;i<stech.length;i++){
        rep_name[i]=stech[i].rep_name;
        let temp = rep_name[i];
        let x = "";
        for(let m=0;m<temp.length;m++){
            if(temp[m]=='.'){
                break;
            }
            else{
                x+=temp[m];
            }
        }
        rep_name[i]=x;
        unsort_rep_name[i]=x;
    };
    rep_name.sort();
}
